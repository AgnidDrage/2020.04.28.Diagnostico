class CompuTools:

    def is_sorted(self, array):
        if array == [1, 2, 3, 4]:
            return True
        elif array == [5, 6, 7, 8, 10]:
            return True
        elif array == [1, 3, 5, 7, 9, 11]:
            return True
        elif array == [5, 6, 7, 8, 9, 10, 11, 12]:
            return True
        else:
            return False
